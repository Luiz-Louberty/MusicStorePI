    public void atualizarTabela() {
        try {
            InstrumentoDAO dao = new InstrumentoDAO();
            List<Instrumento> lista = dao.listarTodos();

            DefaultTableModel modelo = (DefaultTableModel) tblInstrumentos.getModel();
            modelo.setNumRows(0); // Limpa a tabela antes de preencher

            for (Instrumento i : lista) {
                modelo.addRow(new Object[]{
                    i.getId(),
                    i.getNome(),
                    i.getMarca(),
                    i.getCategoria(),
                    i.getPreco(),
                    i.getQuantidade()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar dados: " + e.getMessage());
        }
    }

