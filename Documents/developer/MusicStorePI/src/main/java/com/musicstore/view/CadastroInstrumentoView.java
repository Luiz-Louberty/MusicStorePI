    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty() || txtPreco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha os campos obrigatórios.");
        } else {
            try {
                Instrumento novoInstrumento = new Instrumento();
                novoInstrumento.setNome(txtNome.getText());
                novoInstrumento.setMarca(txtMarca.getText());
                novoInstrumento.setCategoria(cbCategoria.getSelectedItem().toString());
                novoInstrumento.setPreco(Double.parseDouble(txtPreco.getText()));
                novoInstrumento.setQuantidade(Integer.parseInt(txtQuantidade.getText()));

                InstrumentoDAO dao = new InstrumentoDAO();
                dao.cadastrar(novoInstrumento);

                JOptionPane.showMessageDialog(this, "Instrumento salvo com sucesso no Banco de Dados!");

                btnLimparActionPerformed(evt);

                // ADICIONE ESTA LINHA AQUI:
                atualizarTabela();

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Erro: Preço e Quantidade devem ser números válidos.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao salvar no banco: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblInstrumentos.getSelectedRow();

        if (linhaSelecionada >= 0) {
            int confirmacao = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este instrumento?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                try {
                    
                    int id = (int) tblInstrumentos.getValueAt(linhaSelecionada, 0);

                    InstrumentoDAO dao = new InstrumentoDAO();
                    dao.excluir(id);

                    JOptionPane.showMessageDialog(this, "Instrumento excluído com sucesso!");
                    atualizarTabela(); // Atualiza a grade visual

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um instrumento na tabela para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

